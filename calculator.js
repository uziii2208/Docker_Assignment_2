const CalculationOperations = {

    Add: function (a, b) {

        return a + b;

    },



    subtract: function (a, b) {

        return a - b;

    },

    multiple: function (a, b) {

        return a * b;

    },

    divide: function (a, b) {

        return a / b;

    }

}

module.exports = CalculationOperations